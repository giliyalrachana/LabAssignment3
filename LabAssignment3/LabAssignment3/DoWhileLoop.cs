using System;


namespace LabAssignment3
{
    class DoWhileLoop
    {
        static void Main()
        {
            
            int i = 1;
            do
            {
                if (i % 2 != 0 );
                {
                    Console.WriteLine(i);
                }
                i+=2;
            } while (i < 50 );

            Console.ReadLine();

        }
    }
}

